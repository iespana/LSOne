USE LSPOSNET_Audit

IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME = 'TAXTABLELog' AND COLUMN_NAME = 'TAXNAME' AND CHARACTER_MAXIMUM_LENGTH < 60)
BEGIN
	ALTER TABLE TAXTABLELog
	ALTER COLUMN TAXNAME NVARCHAR(60)
END
GO